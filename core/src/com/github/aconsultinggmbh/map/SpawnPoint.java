package com.github.aconsultinggmbh.map;


public class SpawnPoint {

    private float x;
    private float y;

    public SpawnPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
